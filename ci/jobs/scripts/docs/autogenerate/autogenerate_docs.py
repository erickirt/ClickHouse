FRONTMATTER_RE = re.compile(r"\A---\n.*?\n---\n?", re.DOTALL)


def import_migrate(docs_dir):
    # The Docusaurus->Mintlify body transforms (imports @theme->/snippets,
    # HTML->MDX comments, link rewriting) are reused from the migration script
    # so generated pages match migrated ones exactly -- a single source of truth.
    sys.path.insert(0, os.path.join(docs_dir, "_migration"))
    import migrate  # noqa: E402
    return migrate


def build_file_map(migrate, slug_map):
    # docusaurus_file -> mintlify_file, plus migrate's link/import Lookups.
    lk, rows = migrate.build_lookups(Path(slug_map))
    file_map = {}
    for r in rows:
        mfile = (r.get("mintlify_file") or "").split(" | ")[0].strip()
        if r.get("status") == "matched" and mfile:
            file_map[(r.get("docusaurus_file") or "").strip()] = mfile
    return lk, file_map


def transform_body(migrate, content, src_docu, dest_path, lk):
    # Generated fragments contain the same Docusaurus-flavoured Markdown as
    # whole-page descriptions, so run the complete Mintlify body pipeline.
    return migrate.transform_body_content(
        content,
        title=None,
        lk=lk,
        issues=[],
        source_docu_path=Path(src_docu),
        dest_path=Path(dest_path),
        source_docu_file=src_docu,
        row={"docusaurus_file": src_docu},
    )


TITLE_RE = re.compile(r"^title:\s*(.+?)\s*$", re.MULTILINE)


def frontmatter_title(page_text):
    # The generated body carries no frontmatter -- Mintlify renders the title
    # from the dest page's frontmatter -- so read it there to feed
    # drop_redundant_h1 (which strips a duplicate leading H1 from the body).
    m = FRONTMATTER_RE.match(page_text)
    if not m:
        return None
    t = TITLE_RE.search(m.group(0))
    return t.group(1).strip().strip("'\"") if t else None


def transform_full_body(migrate, content, src_docu, dest_path, lk, title):
    # Whole-page bodies (e.g. a table engine's `Documentation` string, which is
    # the full Markdown body of its reference page) need the complete migrate
    # body pipeline -- drop the redundant H1, convert admonitions/details/tabs,
    # rewrite links and imports -- not just transform_body's narrow subset.
    # Reuse migrate_file's shared entry point so generated pages match migrated
    # ones exactly, from a single source of truth.
    return migrate.transform_body_content(
        content,
        title=title,
        lk=lk,
        issues=[],
        source_docu_path=Path(src_docu),
        dest_path=Path(dest_path),
        source_docu_file=src_docu,
        row={"docusaurus_file": src_docu},
    )


def run_sql(binary, sql_path, cwd, params=None):
    # Run the generator SQL; it writes its INTO OUTFILE markdown into `cwd`.
    cmd = [binary, "local", "--queries-file", sql_path]
    for k, v in (params or {}).items():
        cmd.append(f"--param_{k}={v}")
    subprocess.run(cmd, cwd=cwd, check=True)


def replace_between_markers(page, content):
    start = START_RE.search(page)
    end = END_RE.search(page, start.end()) if start else None
    if not start or not end:
        raise ValueError("destination is missing the AUTOGENERATED markers")
    # Keep the marker lines themselves; replace only the region between them.
    return page[:start.end()] + "\n" + content.strip("\n") + "\n" + page[end.start():]


def is_fully_autogenerated_page(page):
    """Return whether all content below frontmatter is one generated region."""
    frontmatter = FRONTMATTER_RE.match(page)
    if not frontmatter:
        return False
    body = page[frontmatter.end():]
    start = START_RE.search(body)
    if not start or body[:start.start()].strip():
        return False
    end = END_RE.search(body, start.end())
    if not end or body[end.end():].strip():
        return False
    return (
        START_RE.search(body, start.end()) is None
        and END_RE.search(body, end.end()) is None
    )


@dataclass(frozen=True)
class GeneratedArtifact:
    path: Path
    content: str


@dataclass(frozen=True)
class SettingSection:
    name: str
    anchor: str
    markdown: str

    @property
    def tokens(self):
        return tuple(self.name.split("_"))


@dataclass
class SettingPage:
    base_route: str
    parts: tuple[str, ...]
    label: str
    match_prefix: str
    match_mode: str
    sections: list[SettingSection] = field(default_factory=list)
    children: list["SettingPage"] = field(default_factory=list)

    @property
    def route(self):
        return self.base_route + "/" + "/".join(self.parts)

    @property
    def char_count(self):
        return sum(len(section.markdown) for section in self.sections)


def parse_settings_page(content):
    """Split a generated monolithic settings page into its shell and sections."""
    fm = FRONTMATTER_RE.match(content)
    body_start = fm.end() if fm else 0
    body = content[body_start:].lstrip("\n")

    marker_start = START_RE.search(body)
    marker_end = END_RE.search(body, marker_start.end()) if marker_start else None
    if marker_start and marker_end:
        generated_body = body[marker_start.end():marker_end.start()].strip("\n")
        preamble = (
            body[:marker_start.end()].rstrip()
            + "\n\n"
            + body[marker_end.start():].lstrip()
        ).strip("\n")
    else:
        generated_body = body
        preamble = None

    matches = list(SESSION_SETTINGS_HEADING_RE.finditer(generated_body))
    if not matches:
        raise ValueError("settings output contains no setting headings")

    sections = []
    for index, match in enumerate(matches):
        end = matches[index + 1].start() if index + 1 < len(matches) else len(generated_body)
        markdown = generated_body[match.start():end].strip("\n") + "\n"
        markdown = markdown.replace(match.group(0), match.group(0).rstrip(), 1)
        sections.append(SettingSection(
            match.group("name"),
            match.group("anchor"),
            markdown,
        ))
    if preamble is None:
        preamble = generated_body[:matches[0].start()].strip("\n")
    return fm.group(0).rstrip("\n") if fm else "", preamble, sections


def parse_session_settings_page(content):
    return parse_settings_page(content)


def session_settings_page_fits(
        sections,
        max_settings=SESSION_SETTINGS_MAX_PER_PAGE,
        max_characters=SESSION_SETTINGS_MAX_CHARS_PER_PAGE):
    return (
        len(sections) <= max_settings
        and sum(len(section.markdown) for section in sections)
            <= max_characters
    )


def _slugify_group(value):
    slug = re.sub(r"[^a-z0-9]+", "-", value.lower()).strip("-")
    return slug or "other"


def _token_groups(sections, token_count):
    groups = {}
    for section in sections:
        if len(section.tokens) < token_count:
            continue
        token_prefix = "_".join(section.tokens[:token_count])
        groups.setdefault(token_prefix, []).append(section)
    return groups


def group_session_settings(
        sections,
        base_route=SESSION_SETTINGS_BASE_ROUTE,
        max_settings=SESSION_SETTINGS_MAX_PER_PAGE,
        max_characters=SESSION_SETTINGS_MAX_CHARS_PER_PAGE):
    first_token_groups = _token_groups(sections, 1)
    pages = []
    for first_prefix, grouped in sorted(first_token_groups.items()):
        if len(grouped) == 1:
            continue
        second_token_groups = _token_groups(grouped, 2)
        child_groups = {
            prefix: child_sections
            for prefix, child_sections in second_token_groups.items()
            if len(child_sections) >= SESSION_SETTINGS_SECOND_LEVEL_MIN
        }
        child_names = {
            section.name
            for child_sections in child_groups.values()
            for section in child_sections
        }
        parent = SettingPage(
            base_route=base_route,
            parts=(_slugify_group(first_prefix),),
            label=f"{first_prefix}_*",
            match_prefix=first_prefix,
            match_mode="token",
            sections=[section for section in grouped if section.name not in child_names],
        )
        for second_prefix, child_sections in sorted(child_groups.items()):
            second_token = second_prefix.split("_", 1)[1]
            parent.children.append(SettingPage(
                base_route=base_route,
                parts=parent.parts + (_slugify_group(second_token),),
                label=f"{second_prefix}_*",
                match_prefix=second_prefix,
                match_mode="token",
                sections=child_sections,
            ))
        # Do not add a purely structural top-level category when it contains
        # only one page. Promote that page in the explorer/navigation while
        # retaining its two-token label and stable two-segment route.
        if not parent.sections and len(parent.children) == 1:
            pages.append(parent.children[0])
        else:
            pages.append(parent)

    grouped_names = {
        section.name
        for page in walk_setting_pages(pages)
        for section in page.sections
    }
    ungrouped = [section for section in sections if section.name not in grouped_names]
    if ungrouped:
        pages.append(SettingPage(
            base_route=base_route,
            parts=("other",),
            label="Other",
            match_prefix="",
            match_mode="raw",
            sections=ungrouped,
        ))

    oversized = [
        page for page in walk_setting_pages(pages)
        if not session_settings_page_fits(
            page.sections, max_settings=max_settings, max_characters=max_characters)
    ]
    if oversized:
        details = ", ".join(
            f"{page.label} ({len(page.sections)} settings, {page.char_count} characters)"
            for page in oversized
        )
        raise ValueError(
            "two-level settings hierarchy exceeds a page budget: " + details)
    return pages


def walk_setting_pages(pages):
    for page in pages:
        yield page
        yield from walk_setting_pages(page.children)


def session_settings_routes(pages):
    routes = [
        {
            "prefix": page.match_prefix,
            "mode": page.match_mode,
            "target": page.route,
        }
        for page in walk_setting_pages(pages)
        if page.sections
    ]
    mode_priority = {"token": 0, "raw": 1}
    return sorted(
        routes,
        key=lambda route: (-len(route["prefix"]), mode_priority.get(route["mode"], 2), route["target"]),
    )


def setting_route(name, routes):
    setting_name = name.split("-", 1)[0]
    for route in routes:
        prefix = route["prefix"]
        if route["mode"] == "token":
            if setting_name == prefix or setting_name.startswith(prefix + "_"):
                return route["target"]
        elif setting_name.startswith(prefix):
            return route["target"]
    raise ValueError(f"no generated session-settings route for {name}")


def _markdown_heading_title(value):
    """Approximate Mintlify's visible heading text for Markdown inline nodes."""
    value = re.sub(r"!\[([^]]*)\]\([^)]*\)", r"\1", value)
    value = re.sub(r"\[([^]]+)\]\([^)]*\)", r"\1", value)
    value = re.sub(r"\[([^]]+)\]\[[^]]*\]", r"\1", value)
    value = re.sub(r"`+([^`]+?)`+", r"\1", value)
    value = re.sub(r"<[^>]+>", "", value)
    value = value.replace("\\", "")
    return html.unescape(value).replace("*", "").replace("~", "").strip()


def _mintlify_heading_slug(value):
    """Match Mintlify's generated, DOM-facing heading ID for Markdown text."""
    value = re.sub(r"\s+", "-", value.lower().strip())
    # Mintlify first applies encodeURIComponent, then preserves percent escapes
    # while slugifying. Its rendered heading ID decodes those escapes and strips
    # punctuation such as colons, while preserving characters such as `/` and `_`.
    encoded = quote(value, safe="-_.!~*'()")
    preserve_percent = bool(re.search(r"%[0-9A-F]{2}", encoded))
    encoded = encoded.replace("'", "")
    allowed = "A-Za-z0-9%_" if preserve_percent else "a-z0-9_"
    slug = re.sub(rf"[^{allowed}]+", "-", encoded)
    slug = re.sub(r"-+", "-", slug).strip("-")
    return MINTLIFY_HEADING_PUNCTUATION_RE.sub("", unquote(slug))


def _deduplicate_heading_slug(slug, seen_slugs):
    count = seen_slugs.get(slug, 0)
    seen_slugs[slug] = count + 1
    if count == 0:
        return slug
    suffixed = f"{slug}-{count + 1}"
    if suffixed not in seen_slugs:
        seen_slugs[suffixed] = 0
    return suffixed


def _markdown_heading_anchors(markdown, seen_slugs):
    """Yield Mintlify heading IDs in document order, excluding fenced code."""
    fence = None
    for line in markdown.splitlines():
        stripped = line.lstrip()
        fence_match = MARKDOWN_FENCE_RE.match(stripped)
        if fence:
            closes_fence = (
                fence_match
                and fence_match.group("fence")[0] == fence[0]
                and len(fence_match.group("fence")) >= fence[1]
            )
            if closes_fence:
                fence = None
            continue
        if fence_match:
            marker = fence_match.group("fence")
            fence = (marker[0], len(marker))
            continue

        match = MARKDOWN_HEADING_RE.match(stripped)
        if not match:
            continue
        title = re.sub(r"[ \t]+#+[ \t]*$", "", match.group("title"))
        explicit = MARKDOWN_HEADING_ID_RE.search(title)
        if explicit:
            slug = MINTLIFY_HEADING_PUNCTUATION_RE.sub(
                "", unquote(explicit.group("anchor")))
        else:
            slug = _mintlify_heading_slug(_markdown_heading_title(title))
        if slug:
            yield _deduplicate_heading_slug(slug, seen_slugs)


def _settings_anchor_routes(pages, preamble="", source_sections=None):
    """Map every heading anchor moved off an overview page to its new route."""
    anchor_routes = {}
    seen_slugs = {}
    # Headings which stay in the overview still participate in Mintlify's
    # document-wide duplicate-slug counter on the original monolithic page.
    list(_markdown_heading_anchors(preamble, seen_slugs))

    section_routes = {
        section.anchor: page.route
        for page in walk_setting_pages(pages)
        for section in page.sections
    }
    ordered_sections = source_sections or [
        section
        for page in walk_setting_pages(pages)
        for section in page.sections
    ]
    for section in ordered_sections:
        route = section_routes[section.anchor]
        anchors = _markdown_heading_anchors(section.markdown, seen_slugs)
        for anchor in anchors:
            previous = anchor_routes.setdefault(anchor, route)
            if previous != route:
                raise ValueError(
                    f"settings anchor {anchor!r} occurs on both {previous} and {route}"
                )
    return dict(sorted(anchor_routes.items()))


def _resolve_setting_anchor(anchor, routes, anchor_routes):
    for candidate in (anchor, anchor.lower()):
        if candidate in anchor_routes:
            return anchor_routes[candidate], candidate

    base, separator, suffix = anchor.partition("-")
    for candidate in (base, base.lower()):
        if candidate in anchor_routes:
            canonical = candidate + (separator + suffix if separator else "")
            return anchor_routes[candidate], canonical

    try:
        return setting_route(anchor.lower(), routes), anchor
    except ValueError:
        return None


def _rewrite_setting_links(markdown, routes, base_route, anchor_routes=None):
    anchor_routes = anchor_routes or {}

    def absolute_repl(match):
        resolved = _resolve_setting_anchor(
            match.group("anchor"), routes, anchor_routes)
        if not resolved:
            return match.group(0)
        target, anchor = resolved
        return target + "#" + anchor

    markdown = re.sub(
        re.escape(base_route) + r"#(?P<anchor>[A-Za-z0-9_.:-]+)",
        absolute_repl,
        markdown,
    )

    def fragment_repl(match):
        resolved = _resolve_setting_anchor(
            match.group("anchor"), routes, anchor_routes)
        if not resolved:
            return match.group(0)
        target, anchor = resolved
        return "](" + target + "#" + anchor + ")"

    return re.sub(
        r"\]\(#(?P<anchor>[A-Za-z0-9_.:-]+)\)", fragment_repl, markdown)


def _rewrite_session_setting_links(markdown, routes):
    return _rewrite_setting_links(markdown, routes, SESSION_SETTINGS_BASE_ROUTE)


def _component_imports_for_page(preamble, markdown):
    imports = []
    for match in IMPORT_RE.finditer(preamble):
        if re.search(rf"<{re.escape(match.group('symbol'))}\b", markdown):
            imports.append(match.group(0).strip())
    return imports


