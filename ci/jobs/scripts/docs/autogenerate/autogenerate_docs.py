def split_settings_page(dest, content, docs_dir, family_name):
    family = SETTINGS_SPLIT_FAMILIES[family_name]
    root_frontmatter, preamble, sections = parse_settings_page(content)
    pages = group_session_settings(
        sections,
        base_route=family["base_route"],
        max_characters=family["max_characters"],
    )
    routes = session_settings_routes(pages)
    anchor_routes = _settings_anchor_routes(pages, preamble, sections)
    shard_dir = Path(dest).with_suffix("")
    current_routes = {
        page.route for page in walk_setting_pages(pages) if page.sections
    }
    manifest_path = shard_dir / "manifest.json"

    preamble_without_imports = IMPORT_RE.sub("", preamble).strip()
    # Mintlify renders the frontmatter title as the page H1. Some generated
    # Docusaurus bodies repeat that title as a Markdown H1; keep it out of the
    # overview shell so the split page has a single title.
    preamble_without_imports = re.sub(
        r"^# [^\n]+\n+", "", preamble_without_imports, count=1)
    explorer_import = (
        f'import {family["component_name"]} from '
        f'"/{family["component_path"]}";'
    )
    root_content = (
        root_frontmatter + "\n\n" + explorer_import + "\n\n"
        + preamble_without_imports + "\n\n" + _settings_explorer(family)
    )
    artifacts = [GeneratedArtifact(Path(dest), root_content)]
    artifacts.append(GeneratedArtifact(
        Path(docs_dir) / family["component_path"],
        _settings_explorer_component(pages, anchor_routes, family),
    ))

    page_manifest = []
    for page in walk_setting_pages(pages):
        if not page.sections:
            continue
        page_markdown = "\n\n".join(section.markdown.rstrip("\n") for section in page.sections)
        page_markdown = _rewrite_setting_links(
            page_markdown, routes, family["base_route"], anchor_routes)
        imports = _component_imports_for_page(preamble, page_markdown)
        title = f"{page.label} {family['detail_title_suffix']}"
        description = (
            f"ClickHouse {family['description_noun']} in the "
            f"{page.label} generated group."
        )
        body_parts = [
            _session_settings_frontmatter(title, page.label, description),
            AUTOGENERATED_START,
        ]
        if imports:
            body_parts.append("\n".join(imports))
        body_parts.extend([
            family["detail_intro"],
        ])
        if page_markdown:
            body_parts.append(page_markdown)
        body_parts.append(AUTOGENERATED_END)
        page_path = Path(docs_dir) / (page.route.lstrip("/") + ".mdx")
        artifacts.append(GeneratedArtifact(page_path, "\n\n".join(body_parts).rstrip() + "\n"))
        page_manifest.append({
            "path": page.route,
            "label": page.label,
            "settings": len(page.sections),
            "characters": page.char_count,
        })

    navigation = {
        "group": family["navigation_group"],
        "root": family["base_route"].lstrip("/"),
        "directory": "none",
        "pages": [_navigation_entry(page) for page in pages],
    }
    artifacts.append(GeneratedArtifact(
        shard_dir / "navigation.json", json.dumps(navigation, indent=2, sort_keys=False) + "\n"))

    manifest = {
        "version": SESSION_SETTINGS_GENERATOR_VERSION,
        "settings": len(sections),
        "anchorRoutes": anchor_routes,
        "limits": {
            "settingsPerPage": SESSION_SETTINGS_MAX_PER_PAGE,
            "charactersPerPage": family["max_characters"],
            "minimumSecondLevelGroup": SESSION_SETTINGS_SECOND_LEVEL_MIN,
            "maximumPrefixDepth": 2,
        },
        "routes": routes,
        "pages": sorted(current_routes),
        "pageStats": page_manifest,
    }
    artifacts.append(GeneratedArtifact(
        manifest_path, json.dumps(manifest, indent=2, sort_keys=False) + "\n"))
    return artifacts


def split_session_settings_page(dest, content, docs_dir):
    return split_settings_page(dest, content, docs_dir, "session-settings")


def generate(gen, binary, docs_dir, repo_root, migrate, lk, file_map, remap):
    scratch = tempfile.mkdtemp(prefix="autogen-")
    # Stage the files the SQL reads via file() (C++ sources, etc.) into scratch.
    for staged, src in gen.get("deps", {}).items():
        shutil.copyfile(os.path.join(repo_root, src), os.path.join(scratch, staged))
    for sql in gen["sql"]:
        run_sql(binary, os.path.join(SQL_DIR, sql), scratch, gen.get("params"))
    with open(os.path.join(scratch, gen["outfile"]), encoding="utf-8") as f:
        content = f.read()
    if gen.get("skip_if_empty") and not content.strip():
        return None, None  # e.g. a page whose name is not an aggregate function

    dest_rel = file_map.get(gen["dest"]) if remap else re.sub(r"^docs/", "", gen["dest"])
    if not dest_rel:
        raise ValueError(f"no Mintlify destination for {gen['dest']}")
    dest = os.path.join(docs_dir, dest_rel)

    if gen.get("fixups"):
        content = apply_fixups(content)
    if remap:
        if gen.get("full_transform"):
            with open(dest, encoding="utf-8") as f:
                title = frontmatter_title(f.read())
            content = transform_full_body(migrate, content, gen["dest"], dest, lk, title)
        else:
            content = transform_body(migrate, content, gen["dest"], dest, lk)

    if gen["method"] == "markers":
        with open(dest, encoding="utf-8") as f:
            new = replace_between_markers(f.read(), content)
    elif gen["method"] == "body":
        # Keep the Mintlify page's frontmatter; replace the rest with the
        # generated body (its own Docusaurus frontmatter stripped).
        with open(dest, encoding="utf-8") as f:
            fm = FRONTMATTER_RE.match(f.read())
        body = FRONTMATTER_RE.sub("", content, count=1).lstrip("\n")
        new = (fm.group(0).rstrip("\n") + "\n\n" + body) if fm else body
    else:  # overwrite
        new = content
    return dest, new


def generate_artifacts(gen, binary, docs_dir, repo_root, migrate, lk, file_map, remap):
    dest, content = generate(gen, binary, docs_dir, repo_root, migrate, lk, file_map, remap)
    if content is None:
        return []
    if gen["name"] in SETTINGS_SPLIT_FAMILIES and remap:
        return split_settings_page(dest, content, docs_dir, gen["name"])
    return [GeneratedArtifact(Path(dest), content)]


def stale_settings_page_paths(family_name, artifacts, docs_dir):
    """Find generated detail pages that are absent from the current artifact set."""
    family = SETTINGS_SPLIT_FAMILIES[family_name]
    shard_dir = (
        Path(docs_dir) / family["base_route"].lstrip("/")
    ).resolve()
    current_pages = {
        artifact.path.resolve()
        for artifact in artifacts
        if artifact.path.suffix == ".mdx"
        and is_fully_autogenerated_page(artifact.content)
    }
    outside_shard = [
        path for path in current_pages if not path.is_relative_to(shard_dir)
    ]
    if outside_shard:
        raise ValueError(
            f"generated {family_name} page is outside {shard_dir}: "
            + ", ".join(str(path) for path in sorted(outside_shard))
        )
    if not shard_dir.is_dir():
        return []

    stale = []
    for path in sorted(shard_dir.rglob("*.mdx")):
        if path.is_symlink():
            raise ValueError(f"refusing to inspect generated-page symlink: {path}")
        if path.resolve() in current_pages:
            continue
        if is_fully_autogenerated_page(path.read_text(encoding="utf-8")):
            stale.append(path)
    return stale


def reconcile_generated_artifacts(
        artifacts, stale_paths, docs_dir, *, check=False, write=False):
    """Check, write, or preview one generator's current and obsolete artifacts."""
    docs_root = Path(docs_dir).resolve()
    drift = 0
    for path in stale_paths:
        rel = os.path.relpath(path.resolve(), docs_root)
        if check:
            print(f"DRIFT: {rel} is an obsolete generated page")
            drift += 1
        elif write:
            path.unlink()
            print(f"deleted: {rel}")
        else:
            print(f"[dry-run] would delete: {rel}")

    for artifact in artifacts:
        dest, new = artifact.path, artifact.content
        rel = os.path.relpath(dest.resolve(), docs_root)
        if check:
            current = dest.read_text(encoding="utf-8") if dest.is_file() else ""
            if current != new:
                print(f"DRIFT: {rel} is stale")
                drift += 1
            else:
                print(f"ok: {rel}")
        elif write:
            dest.parent.mkdir(parents=True, exist_ok=True)
            dest.write_text(new, encoding="utf-8")
            print(f"wrote: {rel}")
        else:
            print(f"[dry-run] would update: {rel}")
    return drift


def main(argv=None):
    p = argparse.ArgumentParser(description=__doc__,
                                formatter_class=argparse.RawDescriptionHelpFormatter)
    p.add_argument("--binary", default="build/programs/clickhouse")
    p.add_argument("--docs-dir", default="docs")
    p.add_argument("--slug-map",
                   help="default: <docs-dir>/_migration/slug-map.csv")
    mode = p.add_mutually_exclusive_group()
    mode.add_argument("--write", action="store_true", help="update docs in place")
    mode.add_argument("--check", action="store_true",
                      help="fail if regenerated output differs from committed docs")
    p.add_argument("--no-remap-legacy", dest="remap", action="store_false",
                   help="emit paths/links as the SQL produces them (Mintlify-native)")
    p.add_argument("--only", help="only run generators whose name contains this substring")
    args = p.parse_args(argv)

    binary = os.path.abspath(args.binary)
    docs_dir = os.path.abspath(args.docs_dir)
    repo_root = os.path.dirname(docs_dir)
    slug_map = args.slug_map or os.path.join(docs_dir, "_migration", "slug-map.csv")

    # The component-reference families (table/database engines, data types, formats,
    # table/window functions) discover their pages by iterating the slug map
    # (file_map), so build it even for a --no-remap-legacy run: those generators'
    # names are what the fast-fail check needs to tell whether the current selection
    # targets a family such a run cannot produce. The link/import remapping itself
    # only runs under --remap-legacy, so drop `migrate`/`lk` afterwards in that mode.
    migrate = import_migrate(docs_dir)
    lk, file_map = build_file_map(migrate, slug_map)
    if not args.remap:
        migrate = lk = None

    remap_only_families = [
        table_engine_generators,
        database_engine_generators,
        data_type_generators,
        format_generators,
        table_function_generators,
        window_function_generators,
    ]

    all_generators = ALL_GENERATORS + aggregate_generators(docs_dir)
    legacy_generators = []
    for builder in remap_only_families:
        legacy_generators += builder(docs_dir, file_map)

    # `--only` is a plain substring match on generator names; apply exactly that in
    # both the fast-fail check and the final selection so the two can never disagree
    # (a substring that targets a family, e.g. `--only newjson`, must be caught).
    def selected(gens):
        return [g for g in gens if not args.only or args.only in g["name"]]

    if args.remap:
        all_generators += legacy_generators
    else:
        # --no-remap-legacy is a forward-looking mode for after the generation
        # source is updated to emit Mintlify-native paths (see the module
        # docstring). Today no family can be produced in it:
        #   * the component-reference families (table/database engines, data
        #     types, formats, table/window functions) are discovered through the
        #     slug map and need its link/path remapping to even be enumerated;
        #   * settings, functions and aggregate carry hard-coded Docusaurus
        #     `dest` paths (e.g. docs/operations/settings/settings.md,
        #     docs/sql-reference/aggregate-functions/reference/<fn>.md) whose
        #     Mintlify page lives elsewhere, so the naive `re.sub(r"^docs/", "")`
        #     in generate() resolves them to files that do not exist in the
        #     Mintlify tree -- open(dest) would fail with FileNotFoundError once
        #     the SQL has run.
        # So fail fast for the whole selection (a full run, or an --only that
        # matches any generator) instead of dying mid-run, or -- for an --only
        # that matches nothing -- silently generating nothing.
        blocked = selected(all_generators) + selected(legacy_generators)
        if blocked:
            families = sorted({g["name"].split(":", 1)[0] for g in blocked})
            raise SystemExit(
                "error: --no-remap-legacy cannot generate the "
                f"{', '.join(families)} pages -- the generation source still "
                "emits Docusaurus-style paths and links, which only "
                "--remap-legacy (the default) translates to their Mintlify "
                "equivalents via the slug map (_migration/slug-map.csv). Re-run "
                "with --remap-legacy until the generation source is updated to "
                "emit Mintlify-native paths.")

    generators = selected(all_generators)
    # A selector that matches nothing is a mistake (a typo, or a family unavailable
    # in this mode); never report success while doing nothing.
    if args.only and not generators:
        raise SystemExit(f"error: --only '{args.only}' matched no generator; nothing to do.")

    drift = 0
    for gen in generators:
        artifacts = generate_artifacts(
            gen, binary, docs_dir, repo_root, migrate, lk, file_map, args.remap)
        stale_paths = []
        if gen["name"] in SETTINGS_SPLIT_FAMILIES and args.remap:
            stale_paths = stale_settings_page_paths(
                gen["name"], artifacts, docs_dir)
        drift += reconcile_generated_artifacts(
            artifacts,
            stale_paths,
            docs_dir,
            check=args.check,
            write=args.write,
        )
    return 1 if drift else 0


if __name__ == "__main__":
    sys.exit(main())
