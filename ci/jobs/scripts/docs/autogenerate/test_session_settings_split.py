#!/usr/bin/env python3
"""Focused tests for the generated flat settings-page hierarchy."""

import importlib.util
import json
import sys
import tempfile
from pathlib import Path

HERE = Path(__file__).resolve().parent


def load_module():
    spec = importlib.util.spec_from_file_location(
        "autogenerate_docs", HERE / "autogenerate_docs.py")
    mod = importlib.util.module_from_spec(spec)
    sys.modules[spec.name] = mod
    spec.loader.exec_module(mod)
    return mod


def generated_page(names, body_h1=None):
    sections = []
    for setting in names:
        name, anchor = setting if isinstance(setting, tuple) else (setting, setting)
        related = ""
        if name == "filesystem_cache_alpha":
            related = (
                "\nSee [force_aggregate](#force_aggregate_partitions_independently) "
                "and its [example](#force_aggregate_partitions_independently-example).\n\n"
                "### OpenSSL compatibility {#openssl}\n\n"
                "See [OpenSSL](#openSSL).\n\n"
                "### Parallel processing using `SAMPLE` key\n\n"
                "Details.\n\n"
                "### Resize Node: Inputs/Outputs\n\n"
                "More details.\n\n"
                "```text\n"
                "### Not a rendered heading\n"
                "```\n"
            )
        sections.append(
            f"## {name} {{#{anchor}}}\n\n"
            f'<SettingsInfoBlock type="Bool" default_value="0" />\n\n'
            f"Description for {name}.{related}\n")
    heading = f"# {body_h1}\n\n" if body_h1 else ""
    return (
        "---\n"
        "title: 'Session Settings'\n"
        "sidebarTitle: 'Session Settings'\n"
        "description: 'Settings.'\n"
        "---\n\n"
        'import SettingsInfoBlock from "/snippets/components/SettingsInfoBlock/SettingsInfoBlock.jsx";\n\n'
        + heading
        + "{/* Autogenerated */}\n"
        "Generated introduction.\n\n"
        + "".join(sections)
    )


def marker_generated_page(names):
    page = generated_page(names)
    first_heading = f"## {names[0]} {{#{names[0]}}}"
    return (
        page.replace(
            first_heading,
            "{/*AUTOGENERATED_START*/}\n" + first_heading,
            1,
        ).rstrip()
        + "\n{/*AUTOGENERATED_END*/}\n"
    )


def setting_names(mod, content):
    return {match.group("name") for match in mod.SESSION_SETTINGS_HEADING_RE.finditer(content)}


def main():
    mod = load_module()
    mod.SESSION_SETTINGS_MAX_PER_PAGE = 20
    mod.SESSION_SETTINGS_MAX_CHARS_PER_PAGE = 100_000
    mod.SESSION_SETTINGS_PREFIX_GROUP_MIN = 2

